public class playToplay {

    private bool conf;

    private double totalDinero;

    private double valApostar;

    private bool winner = false;

    public bool Conf {
        get { return conf; }
        set { conf = value; }
    }

    public double TotalDinero {
        get { return totalDinero; }
        set { totalDinero = value; }
    }

    public double ValApostar {
        get { return valApostar; }
        set { valApostar = value; } 
    }

    public bool Winner {
        get { return winner; }
        set { winner = value; } 
    }
    
}